package taskcevaplari.Tasks10_forLoop;

public class Odev01 {

    public static void main(String[] args) {

        //    0 ile 100 arasındaki bütün çift sayıları print eden code create ediniz.
        //Not: 0 ve 100 dahildir.

        //Kodu aşağıya yazınız.
        int i;
        for (i = 1; i < 100; i++) {

            if (i % 2 == 0)

                System.out.print(i + " ");
        }


    }
}
